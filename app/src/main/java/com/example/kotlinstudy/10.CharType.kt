package com.example.kotlinstudy

fun main(args: Array<String>): Unit {
    var ch: Char = 'A' // 2byte
    println(ch) // 결과 : A

    ch = '\uAC00' // unicode (0 ~ 65535) (0 ~ FFFF)
    println(ch) // 결과 : 가

    ch = '가'
    println(ch.toInt()) // 결과 : 44032
}

